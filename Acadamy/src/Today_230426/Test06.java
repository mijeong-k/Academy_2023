package Today_230426;

public class Test06 {

	public static void main(String[] args) {
		int var1 = 10;
		int var2 = 012;
		int var3 = 0xA;
		
//		012(0으로 시작하는 숫자. 8진수)를 10진수로 변환 = 1*8^1 + 2*8^0 = 10
//		0xA(16진수)를 10진수로 변환 = 10
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);

	}

}
